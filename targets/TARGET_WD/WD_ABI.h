#ifndef WD_ABI_H_
#define WD_ABI_H_

#define  WD_FLASH_BASE	0x08000000

/*

	ABI Header

	The ABI Header is generated by Compiler, post-processed by the ELF-Tool, and read by the bootloader.

	Common:
	00: int MAGIC
		FFFE:	Common Magic
		    01:		Bootloader
		    02:		Application
			  01	has CRC padding flag
			  02	has digital Signature flag
			  04	has KeyStore flag
	04: int SIZE (0 by default, written by ELF-Tool)
	08: int CPUID-MASK:	FFF0FFF0
	0C: int CPUID:		4100C240

	Additional fields for Application:
	10: char NAME[32];
	30: char VERSION[32];

	Additional fields for Bootloader:
	10: fnptr srvcall;
	14: int bllen;
	18: int bank2address;
	
	Example Application-Header:
	MAGIC: FFFE0103 -> Application-Header. Image is CRC32 padded and contains a digital signature
	SIZE: 0x67658
	CPUID-MASK:	FFF0FFF0
	CPUID:		4100C240
	NAME:  "test-app\0\0\0\0..." C-String padded to 32 chars
	VERSION: "1.1\0\0\0..."	 C-String padded to 32 chars

	Example Bootloader-Header:
	MAGIC: FFFE0203 -> Bootloader-Header. Image is CRC32 padded and contains a digital signature. If the Bootloader is signed, app have to be signed as well
	SIZE: 0x3420
	CPUID-MASK:	FFF0FFF0
	CPUID:		4100C240
	SRVCALL: 0x0800024C
	LENGTH: 0x4000	-> for core-board this is sector #0
	BANK2OFF: 0x08100000

	<image data>

	if(WD_ABI_HDR_FLAG_CRC)
		int CRC32-PADDING: Padded with CRC32 sum, so we will get 0 in a stream-crc validation

	if(WD_ABI_HDR_FLAG_DSA)
		int WD_ABI_DSA_SIGNATURE_MAGIC: 0x01000060 (can be read like V01, Len=0x60)
		char key_thumbprint[0x20];
		char sig_r[0x20];
		char sig_s[0x20];

*/

// ABI Values
#define WD_ABI_HDR_OFFSET	0x200		// this is the start offset from to the ABI Header. This value is chosen to be > than sizeof(vector-table)
#define WD_ABI_HDR_MAGIC	0xFFFE		// this is the high-word of the header, which is constant. The low word specifies the kind of the image
#define WD_ABI_HDR_BL		0x01		// this is a header of the bootloader
#define WD_ABI_HDR_APP		0x02		// this is a header of the application
#define WD_ABI_HDR_FLAG_CRC	0x01
#define WD_ABI_HDR_FLAG_DSA	0x02
#define WD_ABI_HDR_FLAG_KEYSTR	0x04

// offsets needed in firmware
#define WD_ABI_HDR_OFFSET_SIZE		0x04
#define WD_ABI_HDR_OFFSET_CPUMASK	0x08
#define WD_ABI_HDR_OFFSET_CPUID		0x0C

#define WD_ABI_BLHDR_OFFSET_SRVCALL	0x20	
#define WD_ABI_APPHDR_OFFSET_NAME	0x20
#define WD_ABI_APPHDR_OFFSET_VERSION	0x40


// Size Constants
#define WD_ABI_SIZE_CRC		0x04
#define WD_ABI_SIZE_KEYSTR	0x64
#define WD_ABI_SIZE_SIGNATURE 0x64

// Magics
#define WD_ABI_KEYSTR_MAGIC	0xCC000001


// ABI Field-Offsets
#define WD_ABI_HDR_FLDOFF_MAGIC	0x00
#define WD_ABI_HDR_FLDOFF_SIZE	0x04

#define WD_ABI_HDRBL_FLDOFF_SRVCALL	0x08
#define WD_ABI_HDRBL_FLDOFF_LENGTH	0x0C
#define WD_ABI_HDRBL_FLDOFF_BANK2	0x10
#define WD_ABI_HDRBL_FLDOFF_KEYSTR	0x14

#define WD_ABI_HDRAPP_FLDOFF_NAME	0x08
#define WD_ABI_HDRAPP_FLDOFF_VERSION 0x28

#define WD_ABI_BL_HEADER_OFFSET				0x200
#define WD_ABI_BL_HEADER_FLDOFF_SRVCALL		0x04
#define WD_ABI_BL_HEADER_FLDOFF_LENGTH		0x08	// Length = total section length
#define WD_ABI_BL_HEADER_FLDOFF_SIZE		0x0C	// Size = consumed size incl. CRC word
#define WD_ABI_BL_HEADER_FLDOFF_BANK2OFFSET	0x10
#define WD_ABI_BL_HEADER_FLDOFF_KEYSTORE	0x14

#define WD_ABI_BL_HEADER_MAGIC				0x0102	// this is placed in the high halfword of the flags

// the following declarations are for BL-HEADER v1 (Magic = 0102)
#define WD_ABI_BL_HEADER_FLAG_PRODUCTION	0x01	// only valid images are accepted by the bootloader in general, if there is a DSAKEY they have to be signed

#define WD_ABI_METADATA_SECTION_OFFSET		0x300
#define WD_ABI_METADATA_MAGIC				0x01020304
#define WD_ABI_KEYSTORE_CURRENT_VERSION		0xCC000001
#define WD_ABI_SIGNATURE_CURRENT_VERSION	0x01000040

// these constants specify the offsets of the fields in the .metadata section
#define WD_ABI_METADATA_FLDOFF_SIZE					0x04
#define WD_ABI_METADATA_FLDOFF_APPLICATION_NAME		0x08
#define WD_ABI_METADATA_FLDOFF_APPLICATION_VERSION	0x28
#define WD_ABI_METADATA_FLDOFF_CPUID_MASK			0x48
#define WD_ABI_METADATA_FLDOFF_CPUID				0x4C

#define WD_ABI_APPLICATION_NAME_SIZE	(WD_ABI_METADATA_FLDOFF_APPLICATION_VERSION-WD_ABI_METADATA_FLDOFF_APPLICATION_NAME)
#define WD_ABI_APPLICATION_VERSION_SIZE	(WD_ABI_METADATA_FLDOFF_CPUID_MASK-WD_ABI_METADATA_FLDOFF_APPLICATION_VERSION)

#endif /* WD_ABI_H_ */